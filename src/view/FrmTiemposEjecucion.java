    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        // TODO add your handling code here:
        int op = JOptionPane.showConfirmDialog(this, "Se dispone a modificar datos de tiempos\na de intercambio de informaci贸n entre\nlas bases de datos del sistema \n"
                + "驴desea continuar?", "Cambio de tiempos de ejecuci贸n", JOptionPane.OK_CANCEL_OPTION);
        if (JOptionPane.OK_OPTION == op) {
            op = JOptionPane.showConfirmDialog(this, "Confirme, 驴Desea hacer permanentes los cambios?"
                    + "驴desea continuar?", "Cambio de tiempos de ejecuci贸n", JOptionPane.OK_CANCEL_OPTION);
            if (JOptionPane.OK_OPTION == op) {
                if (txt_TWinsis.getText().isEmpty() || txt_Tagilis.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Debe ingresar los nuevos tiempos\nde ejecuci贸n para el intercambio de datos");
                } else {
                    prop.setProperty("timeAgilis", String.valueOf(Integer.parseInt(txt_Tagilis.getText()) * 1000));
                    prop.setProperty("timeWinsislab", String.valueOf(Integer.parseInt(txt_TWinsis.getText()) * 1000));
                    try {
                        PropertiesIO.write(prop, ruta);
                        JOptionPane.showMessageDialog(null, "Guardado");
                        btn_save.setEnabled(false);
                        txt_TWinsis.setEnabled(false);
                        txt_Tagilis.setEnabled(false);
                    } catch (IOException ex) {
                        JOptionPane.showMessageDialog(null, "Ocurri贸 el siguiente problema al\nmodificar el archivo config.properties:\n" + ex.getMessage());
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "Se cancel贸 el proceso de cambio de tiempos");
                btn_save.setEnabled(false);
                txt_Tagilis.setText("" + (Integer.parseInt(prop.getProperty("timeAgilis")) / 1000));
                txt_TWinsis.setText("" + (Integer.parseInt(prop.getProperty("timeWinsislab")) / 1000));
                txt_TWinsis.setEnabled(false);
                txt_Tagilis.setEnabled(false);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Se cancel贸 el proceso de cambio de tiempos");
            btn_save.setEnabled(false);
            txt_Tagilis.setText("" + (Integer.parseInt(prop.getProperty("timeAgilis")) / 1000));
            txt_TWinsis.setText("" + (Integer.parseInt(prop.getProperty("timeWinsislab")) / 1000));
            txt_TWinsis.setEnabled(false);
            txt_Tagilis.setEnabled(false);
        }
    }//GEN-LAST:event_btn_saveActionPerformed

