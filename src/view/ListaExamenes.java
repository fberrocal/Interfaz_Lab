    private void cb_filtroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cb_filtroActionPerformed
        if (String.valueOf(cb_filtro.getSelectedItem()).equals(" ") && !btn_filtro.isEnabled()) {
            JOptionPane.showMessageDialog(null, "Seleccione un criterio para Filtrar");
        } else {
            jTextField1.setText(null);
            jTextField1.setEnabled(true);
            jTextField1.setText("%");
            jTextField1.requestFocus();
            btn_filtro.setEnabled(true);
        }
    }//GEN-LAST:event_cb_filtroActionPerformed

    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        // TODO add your handling code here:
        tab_examenes.setModel(controlBD.filtrolistadoTablaExamenes(con, jTextField1.getText(), String.valueOf(cb_filtro.getSelectedItem())));
        tab_examenes.getColumn("Nombre").setPreferredWidth(600);
    }//GEN-LAST:event_jTextField1KeyReleased

    private void btn_filtroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_filtroActionPerformed
        // TODO add your handling code here:
        if (jTextField1.isEnabled()) {
            cb_filtro.setSelectedIndex(0);
            jTextField1.setText(null);
            jTextField1.setEnabled(false);
            tab_examenes.setModel(controlBD.listadoTablaExamenes(con));
            tab_examenes.getColumn("Nombre").setPreferredWidth(600);
            btn_filtro.setEnabled(false);
        }
    }//GEN-LAST:event_btn_filtroActionPerformed

