    public void retorno() {
        if (tab_examenes.getSelectedColumn() != -1) {
            idWinsislab = String.valueOf(tab_examenes.getValueAt(tab_examenes.getSelectedRow(), 0));
            HomologacionHexamenes.txt_idwinsislab.setText(idWinsislab);
        } else {
            idWinsislab = null;
        }
    }

